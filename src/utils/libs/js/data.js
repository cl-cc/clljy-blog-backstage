var _interval = null;
var _index = 0;
function realNaviStart(points, callback) {
    if (_interval) {
        clearInterval(_interval);
        _index = 0;
    };
    // 创建定时器
    _interval = setInterval(() => {
        // 到达终点
        if (_index > points.length - 1) {
            callback(false);
            clearInterval(_interval);
            _index = 0;
            return;
        }
        callback(points[_index]);
        _index++;
    }, 200);
}

/** 真实导航结束 */
function realNaviStop(params) {
    if (_interval) {
        clearInterval(_interval);
        _index = 0;
    };
}

/** 给Date增加Format格式化方法 */
Date.prototype.Format = function (fmt) {
    var o = {
        "M+": this.getMonth() + 1,
        "d+": this.getDate(),
        "H+": this.getHours(),
        "m+": this.getMinutes(),
        "s+": this.getSeconds(),
        "S+": this.getMilliseconds()
    };
    if (/(y+)/.test(fmt)) {
        fmt = fmt.replace(RegExp.$1, (this.getFullYear() + "").substr(4 - RegExp.$1.length));
    }
    for (var k in o) {
        if (new RegExp("(" + k + ")").test(fmt)) {
            fmt = fmt.replace(RegExp.$1, (RegExp.$1.length == 1) ? (o[k]) : (("00" + o[k]).substr(String(o[k]).length)));
        }
    }
    return fmt;
};

